//Mathematical ops
var sum = 10 + 10;
console.log(sum); //20

var diff = 45 - 33;
console.log(diff); //12

var product = 44 * 30;
console.log(product); //1320

var div = 66 / 33;
console.log(div); //2

//Inc/dec
sum++;
console.log(sum);

diff--;
console.log(diff);

//Floating point arithmatics
product = 2.0 * 2.5;
console.log(product); //5 not 5.0

var remainder = 11 % 3.0;
console.log(remainder); //2

//Shorthand assignment operators (Compound assignment)
var shortHandSum = 1;
shortHandSum += 100;
console.log(shortHandSum); //101
